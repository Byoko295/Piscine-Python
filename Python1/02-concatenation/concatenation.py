"""
Fonction qui concatene des strings
"""


__author__ = "benoui_m"


def concatenate_strings(str1: str, str2: str) -> str:
    """
    outputs concatenation of two strings
    :param str1: premiere string
    :type str1: string
    :param str2: deuxiemme string
    :type str2: string
    :return: nouvelle string composé de str1 + str2
    """
    return (str1 + ' ' + str2)
